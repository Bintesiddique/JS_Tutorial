
// Variables in JS
// => Variables are containers for data

// Variable Rules
// * Variable names are case sensitive; "a" & "A" is different.
// * Only letters, digits, underscore(_) and $ is allowed. (not even space)
// * Only a letter, underscore() or $ should be 1st character.
// * Reserved words cannot be variable names.

// string value

// fullName = "Areeba  s";
// console.log(fullName);

// numbers value
age = 20;
console.log(age);
// null value
x = null;
console.log(x);
// undefined value
y = undefined;
console.log(y);
// by using let
let fullName = "Ariba";
console.log(fullName);
// by using var
var f_name ="Areeba";
console.log(f_name);
// by using constant variable
const PI = 3.14
console.log(PI);
